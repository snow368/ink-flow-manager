export function normalizeLanguage(input?: string | null): AppLanguage {
  const raw = (input || '').toLowerCase();
  if (raw.startsWith('es')) return 'es';
  if (raw.startsWith('pt')) return 'pt';
  if (raw.startsWith('fr')) return 'fr';
  if (raw.startsWith('de')) return 'de';
  if (raw.startsWith('th')) return 'th';
  if (raw.startsWith('ja') || raw.startsWith('jp')) return 'jp';
  return 'en';
}

export function detectInitialLanguage(): AppLanguage {
  const stored = getStoredLanguage();
  if (stored) return stored;
  return normalizeLanguage(navigator.language);
}

export function getStoredLanguage(): AppLanguage | null {
  const stored = localStorage.getItem(STORAGE_KEY);
  if (!stored) return null;
  return SUPPORTED.includes(stored as AppLanguage) ? (stored as AppLanguage) : null;
}

export function setStoredLanguage(lang: AppLanguage) {
  localStorage.setItem(STORAGE_KEY, lang);
  document.documentElement.lang = lang;
  window.dispatchEvent(new CustomEvent('inkflow_lang_change', { detail: lang }));
}

export function t(lang: AppLanguage, key: string): string {
  return MESSAGES[lang]?.[key] || MESSAGES.en[key] || key;
}
